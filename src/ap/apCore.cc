#include "ap/apCore.h"

namespace Torch {

//////////////////////////////////////////////////////////////////////////
// Constructor

apCore::apCore()
	: 	spCore()
{
	m_audioSize = 0;
}

//////////////////////////////////////////////////////////////////////////
// Destructor

apCore::~apCore()
{
}

//////////////////////////////////////////////////////////////////////////
// Change the input audio size

bool apCore::setAudioSize(int new_length)
{
	if (new_length > 0)
	{
		m_audioSize = new_length;
		return true;
	}
	return false;
}

//////////////////////////////////////////////////////////////////////////
// Retrieve the input audio size

int apCore::getAudioSize() const
{
	return m_audioSize;
}

//////////////////////////////////////////////////////////////////////////
}

